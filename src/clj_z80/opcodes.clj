(defn n?
  [n]
  (let [forbidden-labels #{:a :b :c :d :e :h :l :ixl :ixh :iyl :iyh :r
                           :af :bc :de :hl :ix :iy :sp}]
    (or (number? n)
        (fn? n)
        (and (coll? n)
             (= (count n) 2)
             (contains? #{:low-word :high-word :displacement} (first n)))
        (and (keyword? n)
             (not (contains? forbidden-labels n))))))

