         [:call :m (nn :guard n?)]  [0xfc (lw nn) (hw nn)]
         [:cp (n :guard n?)]        [0xfe (b n)]
         [:rst 0x38]                [0xff]
         [:db (n :guard n?)]        [n]
         [:db (r :guard
                 (fn [n]
                   (and (not (n? n))
                        (coll? n))))] (map b r)
         :else                      nil))

(defn assemble-instr
  [instr]
  (or (assemble-instr-base instr)
      (assemble-instr-fd instr)
      (assemble-instr-dd instr)
      (assemble-instr-cb instr)
      (assemble-instr-ed instr)
      (throw (Exception. (str "Unknown opcode " instr)))))

(defn assemble-instrs
  [instrs]
  (mapcat assemble-instr instrs))
